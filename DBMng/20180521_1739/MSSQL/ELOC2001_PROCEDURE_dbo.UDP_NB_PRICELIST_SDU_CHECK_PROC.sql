-- UDP_NB_PRICELIST_SDU_CHECK_PROC  :   2004.7.20 --
CREATE        PROC UDP_NB_PRICELIST_SDU_CHECK_PROC
	@p_vcode int
AS
	SET NOCOUNT ON
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	
	SELECT PL_NO, PL_MODELNO, PL_VCODE, PL_CATEGORY, PL_GOODSNM, IsNull(PL_PRICE, 0) as PL_PRICE, IsNull(PL_URL, '') as PL_URL
	,PL_NOTE, PL_SRVFLAG, PL_STATUS, PL_ETC, PL_AUTHFLAG, PL_AUTHDATE, PL_SOLDOUTFLAG, PL_SOLDOUTDATE, PL_DATE, PL_RIGHTNLEFT, PL_FLAG, PL_IMGURL, PL_COUPON
	FROM PRICELIST WITH(READUNCOMMITTED)
	WHERE PL_VCODE= @p_vcode AND PL_MODELNO > 0 AND (PL_GOODSNM IS NULL OR LEN(PL_GOODSNM) < 2) AND PL_STATUS < '3'
	
	SET NOCOUNT OFF




