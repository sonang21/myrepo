CREATE PROC USP_PRICELIST_SEL_22
	@PL_VCODE INT,
	@PL_GOODSCODE VARCHAR(50)
AS
BEGIN
	SET NOCOUNT ON
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	SELECT TOP 1
		PL_NO,
		PL_GOODSNM,
		PL_PRICE,
		PL_STATUS
	FROM PRICELIST 
	WHERE PL_VCODE=@PL_VCODE
		AND PL_GOODSCODE = @PL_GOODSCODE
		AND PL_GOODSCODE_CS = CHECKSUM(@PL_GOODSCODE)
		AND PL_GOODSNM<>''		
END