CREATE proc udp_jca_get_month
	@sdate	char(8),
	@edate	char(8)
as
	set nocount on
	set transaction isolation level  read uncommitted
	-- ----------------------------------------------------------------------------------
	-- 작성자 : wookki25 / 2008.03.19
	-- 설  명 : 휴일날짜
	-- 실  행 : udp_jca_get_month '20080301', '20080331'
	-- ----------------------------------------------------------------------------------
	
	-- ----------------------------------------------------------------------------------
	-- 수정내역
	-- ----------------------------------------------------------------------------------
	/*
	수정일:			수정자:			수정내용:
	----------		-----------		-----------------------------------------------------		
	2008.01.25		wookki25		
	*/
	
	select	holiday 
	from	tbl_calendar 
	where	sun_calendar between @sdate and @edate
	order by sun_calendar


