CREATE PROC [dbo].[USP_PRICELIST_UP_13]
	@PL_VCODE INT, 
	@PL_GOODSNM NVARCHAR(200),
	@PL_CATEGORY VARCHAR(8),
	@PL_PRICE MONEY,
	@PL_URL VARCHAR(500),
	@PL_STATUS CHAR(1),
	@PL_RIGHTNLEFT CHAR(1),
	@PL_DELIVERYINFO VARCHAR(100),
	@PL_IMGURL VARCHAR(250),
	@PL_COUPON TINYINT,
	@PL_ACCOUNT_YN CHAR(1),
	@PL_FREEINTEREST NVARCHAR(50)	
AS
BEGIN
	SET NOCOUNT ON
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	DECLARE @GOODSNM NVARCHAR(200)
	SET @GOODSNM = @PL_GOODSNM
	UPDATE PRICELIST
	SET PL_CATEGORY = @PL_CATEGORY,
		PL_PRICE = @PL_PRICE,
		PL_URL = @PL_URL,
		PL_STATUS = @PL_STATUS,
		PL_DATE = GETDATE(),
		PL_RIGHTNLEFT = @PL_RIGHTNLEFT,
		PL_DELIVERYINFO = @PL_DELIVERYINFO,
		PL_IMGURL = @PL_IMGURL,
		PL_COUPON = @PL_COUPON,
		PL_ACCOUNT_YN = @PL_ACCOUNT_YN,
		PL_FREEINTEREST = @PL_FREEINTEREST,
		PL_IMGURLFLAG = (CASE WHEN PL_IMGURL<>@PL_IMGURL THEN '0' ELSE PL_IMGURLFLAG END)
	WHERE PL_VCODE+0 = @PL_VCODE -- 인덱스 타지 않도록 처리 WOOKKI25 2007.070.03
		AND PL_GOODSNM = @GOODSNM
		AND PL_GOODSNM_CS = CHECKSUM(@GOODSNM) 
END







